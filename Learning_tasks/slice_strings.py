# first character is always 0

Hw = "Hello world!"
print(Hw)
print(len(Hw))
print(Hw[0])
print(Hw[-1])
print(Hw[-2])
# doing print(Hw[2:]) prints everything after the second position object onwards to the end of the string
print(Hw[2:])
# doing print(Hw[-3:]) prints everything from the third character from the end onward
print(Hw[-3:])
# doing print(Hw[:5]) prints everything up to character 5
print(Hw[:5])
# the number in the square bracket is the first number NOT included
print(Hw[1:3])
# prints character 1 up to 3 so 1 and 2
