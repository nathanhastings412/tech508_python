# Understanding Operators
# Operators indicate what action or operation to perform
# Operands indicate what items to apply the action to

x = 5
y = 1

print(x + y)
print(x - y)
print(x * y)
print(x / y)
print(x % y)

print(x <= y)
print(x < y)
print(x >= y)
print(x > y)
print(x == y)
print(x != y)

